Project Documentation

1. Project Overview
Project Title: Lou Geh Car Dealership Company Prototype
Description: A web application prototype for a car dealership, featuring customer management, car inventory, sales tracking, and service records.

2. Technologies Used
Visual Studio Code: For code editing and development.
MySQL Command Line: For database management and queries.
PopSQL: For visually managing the database and writing SQL queries.
PHP: Backend development.
HTML, CSS, JavaScript: Frontend development.

