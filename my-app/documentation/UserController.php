5. Installation Guide

Install PHP:

Download PHP from PHP.net.
Follow the installation instructions for your operating system.

Set up MySQL:

Install MySQL and set up your database.
Use the provided SQL code to create the necessary tables.
Setup Project:

Clone or download the project files.
Place the project directory in your web server's root directory.
Access the Application:

Open your web browser and navigate to http://localhost/my-app/index.php.

6. Features
Customer Management: Add customer records.
Car Inventory: add new cars, and edit car details.
Sales Tracking: Record sales transactions.
Service Records: Track service history for each car.

